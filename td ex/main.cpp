// example for counter using if else

#include<iostream>

using namespace std;

int main()

{
    int x=0;

    cout<<"Enter number:";
    cin>>x;

    if (x==2)
    {
        cout<<"x is:"<<x;

    }
    else if (x==1)
    {
        cout<<"x is:"<<x;
    }
    else
    {
        cout<< "x does not exist";
    }
return 0;
}
